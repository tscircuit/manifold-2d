import { readFile, writeFile } from "node:fs/promises"

const root = new URL("../", import.meta.url)
const wasm = await readFile(new URL("vendor/manifold.wasm", root))
const output = `// Generated by scripts/generate-embedded-wasm.mjs. Do not edit.\nexport default ${JSON.stringify(wasm.toString("base64"))}\n`

await writeFile(new URL("vendor/manifold.wasm.base64.js", root), output)
